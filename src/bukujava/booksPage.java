        private void table_update(){
            int c;
            try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/login", "root", "");
            insert = con.prepareStatement("select * from stok_buku");
            
            ResultSet rs = insert.executeQuery();
            ResultSetMetaData Rss = rs.getMetaData();
            c = Rss.getColumnCount();
            
             DefaultTableModel Df = (DefaultTableModel)tabelstok.getModel();
             Df.setRowCount(0);
             
             while(rs.next()){
                 Vector v2 = new Vector();
                 
                 for(int a=1; a<=c; a++){
                     v2.add(rs.getString("id"));
                     v2.add(rs.getString("code"));
                     v2.add(rs.getString("nama"));
                     v2.add(rs.getString("harga"));
                     v2.add(rs.getString("stok"));
                 }
                 Df.addRow(v2);
             }
            
           
            
            } catch (ClassNotFoundException ex) {
            Logger.getLogger(booksPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (SQLException ex) {
            Logger.getLogger(booksPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
