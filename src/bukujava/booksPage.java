    private void edit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit_btnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel Df = (DefaultTableModel)tabelstok.getModel();
        int selectedIndex = tabelstok.getSelectedRow();
        
        try {
            int id = Integer.parseInt(Df.getValueAt(selectedIndex, 0).toString());
            String code = txtcode.getText();
            String nama = txtnama.getText();
            String harga = txtharga.getText();
            String stok = txtstok.getText();
            
            
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/login", "root", "");
            insert = con.prepareStatement("update stok_buku set code=?, nama=?, harga=?, stok=? where id=?");
            insert.setString(1, code);
            insert.setString(2, nama);
            insert.setString(3, harga);
            insert.setString(4, stok);
            insert.setInt(5, id);
            
            insert.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "data berhasil di update");
            table_update();
            
            txtcode.setText("");
            txtnama.setText("");
            txtharga.setText("");
            txtstok.setText("");
            txtnama.requestFocus();
            
            } catch (ClassNotFoundException ex) {
            Logger.getLogger(booksPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (SQLException ex) {
            Logger.getLogger(booksPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_edit_btnActionPerformed

