    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String nama = (txtnama.getText());
        int harga = Integer.parseInt(txtharga.getText());
        int qty = Integer.parseInt(txtqty.getText());
        int tot = qty * harga;
        
        model = (DefaultTableModel)jTable1.getModel();
        
        model.addRow(new Object[]{
            nama,
            harga,
            qty,
            tot,
        });
        int sum = 0;
        for (int i = 0; i<jTable1.getRowCount(); i++){
        sum = sum + Integer.parseInt(jTable1.getValueAt(i,3).toString());    
        }
        
        txttotal.setText(Integer.toString(sum));
        
        txtid.setText("");
        txtnama.setText("");
        txtharga.setText("");
        txtqty.setText("");
        txtid.requestFocus();
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void txtidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtidKeyReleased
        // TODO add your handling code here:
        try{
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/login", "root", "");
                String id = txtid.getText();
                
                    pst = con.prepareStatement("select id,code,nama,harga from stok_buku where code=?");
                    pst.setString(1, id);
                    ResultSet rs = pst.executeQuery();
                    
                if(rs.next()==true){
                    String nama = rs.getString(3);
                    String harga = rs.getString(4);
                    txtnama.setText(nama);
                    txtharga.setText(harga);
                }
                else{
                    txtnama.setText("");
                    txtharga.setText("");
                }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(scanPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (SQLException ex) {
            Logger.getLogger(scanPage.class.getName()).log(Level.SEVERE, null, ex);
        }        
    }//GEN-LAST:event_txtidKeyReleased

